        JMP   start             ; переход на начало программы , это необходима
                                ; Для <освобождения нижней памяти для обработчиков
                                ; прерываний
start:                          ; и потребуется в будущих работах
        ORG   100H              ; директива выделения памяти программ;  место
                                ; расположения
                                ; рабочей программы

        MOV   sp,#AH            ; sp  - имя регистра 

Repeate:                        ; метка возврата
        MOV   A, P0             ; чтение данных из порта в аккумулятор

        MOV   C, A.7            ; заносим 7 бит аккумулятора в c
        ANL   C, A.3            ; c AND A.3
        JC    www
        JB    A.3, xxx
        JB    A.7, xxx
        JMP   www


xxx:    MOV    P1,A             ; выдача результата на порт вывода
        JMP    loop


www:    MOV     P1, #00
        JMP    loop

loop:   DJNZ  RR,Repeate        ;  повторить для новых данных если они не исчерпаны
        JMP $
END
